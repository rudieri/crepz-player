DROP table IF EXISTS condicao cascade;

CREATE TABLE condicao (
id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
playlist SMALLINT FOREIGN KEY REFERENCES PLAYLIST(id),
tipo_operador VARCHAR(128),
operador SMALLINT,
tipo_valor1 SMALLINT,
valor1 VARCHAR(64),
tipo_valor2 SMALLINT,
valor2 VARCHAR(64),
dt_inc BIGINT
);

